public class Auto{
	public static void main(String[] args){
		int valorInicial1= 5;
		System.out.println(" valorInicial++: "+ valorInicial1++);
		int valorInicial2=5;
		System.out.println(" ++valorInicial: "+ ++valorInicial2);
		int valorInicial3=5;
		System.out.println(" valorInicial--: "+ valorInicial3--);
		int valorInicial4=5;
		System.out.println(" --valorInicial: "+ --valorInicial4);
		System.out.println("====================");
		System.out.println(" valorInicial1: "+ valorInicial1);
		System.out.println(" valorInicial2: "+ valorInicial2);
		System.out.println(" valorInicial3: "+ valorInicial3);
		System.out.println(" valorInicial4: "+ valorInicial4);
	}
}

